package ru.maltseva.calcapp.util;

public class Calculator {
    public static int summarize(int num1, int num2) {
        return num1 + num2;
    }

    public static int deduct(int num1, int num2) {
        return num1 - num2;
    }

    public static int multiply(int num1, int num2) {
        return num1 * num2;
    }
}
